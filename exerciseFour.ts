// Boolean
let flag: boolean = true;
console.log("Value assigned to flag is: " + flag);

// Number
let num: number = 5;
console.log("Value for num is " + num);

// String
let s: string = "String";
console.log("String is " + s);

// Array
let list: Array<number> = [1, 2, 3, 4];
console.log("Array contents are " + list[0] + list[1] + list[2] + list[3]);

// Any
let noType: any = 4;
console.log("Any is " + noType);